    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       try {
           conn=Monitor.Enlace(conn);
           res=Monitor.Res(res);
           ResultSetMetaData Res_md = res.getMetaData();
           int cantidad_columnas = Res_md.getColumnCount();
           
           for(int i=1; i <= cantidad_columnas; i++) {
               modelo.addColumn(Res_md.getColumnLabel(i));
           }
           while(res.next()) {
               /* Convertir el resultset en un objeto tablespace 
               Tablespace ts = new Tablespace();
               ts.setName(res.getString("tablespace_name"));
               ts.setTotal_space(res.getInt("totalspace"));
               ts.setFree_space(res.getInt("totalfreespace"));
               ts.setUsed_space(res.getInt("totalusedspace"));               
               */
               
               
               
               Object[] fila = new Object[cantidad_columnas];
               for(int i=0; i < cantidad_columnas; i++) {
                   fila[i] = res.getObject(i+1);
               }
               modelo.addRow(fila);
           }
           res.close();
           conn.close();
       } catch(Exception e) {
           e.printStackTrace();
       }
       
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        jPanel1.removeAll();
        
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        tables.add(new Tablespace("System",23,34,24,85));
        tables.add(new Tablespace("Undo",23,34,24,85));
        tables.add(new Tablespace("Temp",23,34,24,85));
        tables.add(new Tablespace("User",23,34,24,85));

        for(int i=0; i<tables.size(); i++) {
            switch(tables.get(i).getName()) {
                case "System":
                    if(System) 
                        dataset.setValue(tables.get(i).getTotal_space(), "Space", tables.get(i).getName());
                    break;
                    case "Undo":
                    if(Undo) 
                        dataset.setValue(tables.get(i).getTotal_space(), "Space", tables.get(i).getName());
                    break;
                    case "Temp":
                    if(Temp) 
                        dataset.setValue(tables.get(i).getTotal_space(), "Space", tables.get(i).getName());
                    break;
                    case "User":
                    if(User) 
                        dataset.setValue(tables.get(i).getTotal_space(), "Space", tables.get(i).getName());
                    break;
                    case "Index":
                    if(Index) 
                        dataset.setValue(tables.get(i).getTotal_space(), "Space", tables.get(i).getName());
                    break;
            }
        }       
        // dataset.setValue(80, "Space", "Student1");

        JFreeChart chart = ChartFactory.createBarChart("Tablespaces", "Name", "Space", dataset, PlotOrientation.HORIZONTAL, false, true, false);
        CategoryPlot p = chart.getCategoryPlot();
        p.setRangeGridlinePaint(Color.black);
        jPanel1.setLayout(new java.awt.BorderLayout());
        ChartPanel CP = new ChartPanel(chart);
        jPanel1.add(CP,BorderLayout.CENTER);
        jPanel1.validate();
        
    }//GEN-LAST:event_jButton2ActionPerformed

